public class Test10循环结构for
{
    public static void main(String[] args)
    {
        /**for循环结构！！！重点用的多
         * for>while>do...while
         * DRY:Don’t Repeat Yourself
         * for循环用的最多
         */
        for (int i = 0; i < 3; i++)
        {
            for (int j = 1; j <= 3; j++)
            {
                System.out.println("加油加油加油");
            }
        }
    }
}
